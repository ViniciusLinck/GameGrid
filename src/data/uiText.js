export function normalizeLanguage(language = DEFAULT_LANGUAGE) {
  if (language === "pt" || language === "pt-BR") return "pt-BR";
  if (language === "en" || language === "en-US") return "en";
  if (language === "es" || language === "es-ES") return "es";
  return DEFAULT_LANGUAGE;
}

export function getLanguageMeta(language = DEFAULT_LANGUAGE) {
  const normalized = normalizeLanguage(language);
  return LANGUAGE_OPTIONS.find((option) => option.value === normalized) ?? LANGUAGE_OPTIONS[0];
}

export function getUiText(language = DEFAULT_LANGUAGE) {
  return catalog[normalizeLanguage(language)] ?? catalog[DEFAULT_LANGUAGE];
}
